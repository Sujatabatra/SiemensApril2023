function print(arg1,arg2,arg3){
  console.log("print method with three(3) arguments")
  console.log("arg1 : "+arg1);
  console.log("arg2 : "+arg2);
  console.log("arg3 : "+arg3);
}

function print(arg1,arg2){
  console.log("print method with two(2) arguments")
  console.log("arg1 : "+arg1);
  console.log("arg2 : "+arg2);
  
}

function print(arg1,arg2,arg3,arg4,arg5){
  console.log("print method with five(5) arguments")
  console.log("arg1 : "+arg1);
  console.log("arg2 : "+arg2);
  console.log("arg3 : "+arg3);
  console.log("arg4 : "+arg4);
  console.log("arg5 : "+arg5);
}
